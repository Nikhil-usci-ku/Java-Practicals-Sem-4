public class P31_Math_Method {
    public static void main(String[] args) {
        int A = 10;
        int B = 25;
        System.out.println("Bigger number between a and b : "+Math.max(A, B));
        System.out.println("Smaller number between a and b : "+Math.min(A, B));
        System.out.println("integer A raised to power of 2 : "+Math.pow(A, 2));
        System.out.println("integer B reduced to square root : "+Math.sqrt(B));
        System.out.println("Random double between 0 and 1 : "+Math.random());
    }
}
